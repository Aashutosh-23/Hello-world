// There are two types of data types in JAVA
// 1. Primitive Data types
// Primitive Data types consist of
// int --> STORES THE NORMAL INTEGER, byte --> STORES THE NUMBER , float --> STORES THE DECIMAL NUMBER, long --> ISME BADAA INTEGER STORE KRTA HAI , char --> STORES THE CHARACTER, double,--> ISME BADAA FLOATING POINT NUMBER STORE HOTA HAI,  bool --> STORES THE TRUE AND FALSE VALUE, short --> STORE THE INTEGER AS WELL

// DEFAULT TYPE FOR DECIMALS IS double


// 2. Non - Primitive Data types 
    // THESE ARE DERIVED FROM PRIMITIVE DATA TYPES 
    
public class Data_types{
    public static void main(String args[]){
        System.out.println("Resulting Data types");
        byte x = 4;
        short y = 34;
        int z = 34;
        int a = 34+2;
        float o = 6.54f + 6;
        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
        System.out.println(a);
        System.out.println(o);
        
    }
}