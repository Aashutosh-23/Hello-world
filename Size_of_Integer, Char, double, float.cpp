#include<iostream>
using namespace std;
main()
{
	int a;
	char c;
	double d;
	float b;
	
	cout<<"The size of int a is "<<sizeof(a)<<endl;
	cout<<"The size of char c is "<<sizeof(c)<<endl;
	cout<<"The size of double d is "<<sizeof(d)<<endl;
	cout<<"The size of float b is "<<sizeof(b)<<endl;
	
	
 	  return 0;	
}

