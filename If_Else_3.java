public class If_Else_3 {
    // if else if else if ladder 
    public static void main(String args[]){
        int age = 12;
        if(age>50){
            System.out.println("You are experienced");
        }
        else if(age<80){
            System.out.println("You are old");
        }
        else{
            System.out.println("You are not experienced");
        }
    }
}
